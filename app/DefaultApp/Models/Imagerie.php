    private function existe($nom)
    {
        try {
            $con = self::connection();
            $req = "select *from imagerie where nom=:nom";
            $stmt = $con->prepare($req);
            $stmt->execute(array(
                ":nom" => $nom
            ));
            $data = $stmt->fetchAll();
            if (count($data) > 0) {
                return true;
            }
            return false;
        } catch (\Exception $ex) {
            throw new \Exception($ex->getMessage());
        }
    }

    public function findByNom($nom)
    {
        $con = self::connection();
        $req = "select *from imagerie where nom=:nom";
        $stmt = $con->prepare($req);
        $stmt->execute(array(
            ":nom" => $nom
        ));
        $data = $stmt->fetchAll(\PDO::FETCH_OBJ);
        if (count($data) > 0) {
            return $data[0];
        }
        return null;
    }

