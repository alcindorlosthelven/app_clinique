            $req = "SELECT * FROM imagerie where  nom like '%" . $critere . "%' or nom_alternatif like '%" . $critere . "%' or devise like '%" . $critere . "%' or prix like '%" . $critere . "%'  order by nom ASC";
            $stmt = $con->prepare($req);
